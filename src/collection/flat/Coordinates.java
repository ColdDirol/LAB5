package collection.flat;

public class Coordinates {
    private Float x; //Поле не может быть null
    private Float y; //Поле не может быть null

    public Float getX() {
        return x;
    }
    public void setX(Float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }
    public void setY(Float y) {
        this.y = y;
    }
}